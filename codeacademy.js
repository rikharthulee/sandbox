Math.random(); // Create a random number between 0 - 1

Math.random() * 5; // Will create a random decimal number between 0 and 5 - 3.7561876522176796 for example

Math.floor(); // Takes a decimal number and rounds it down to the nearest whole number
console.log(Math.floor(2.7)); // 2.7 rounds down to 2

Math.floor(Math.random() * 10); // Call the Math.floor function and pass it Math.random * 10 - will generate a random number between 0 - 10

Math.ceil(43.8); // Rounds up to the nearest integer (whole number)
